#include <stdio.h>

int main() {
    // 打印第一行的星号
    for (int i = 0; i < 12; i++) {
        printf("*");
    }
    printf("\n");  // 换行

    // 打印 "Welcome"
    printf("  Welcome \n");

    // 打印最后一行的星号
    for (int i = 0; i < 12; i++) {
        printf("*");
    }
   

    return 0;
}
